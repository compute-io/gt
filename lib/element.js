'use strict';

// GREATER THAN //

/**
* FUNCTION: gt( x, y )
*	Checks whether input element x is greater than y
*
* @param {Number|String} x - input value
* @param {Number|String} y - comparator
* @returns {Number} 1 if x is greater than y, 0 otherwise
*/
function gt( x, y ) {
		return x > y ? 1 : 0;
} // end FUNCTION gt()

// EXPORTS //

module.exports = gt;
